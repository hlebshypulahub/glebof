    public static String couponAcceptMessage(final Coupon coupon, final boolean inAllPharmacies, final int durationInMinutes) {
        StringBuilder sb = new StringBuilder(coupon.getText() + "\n\n");

        appendPharmacies(sb, coupon.getPharmacies().stream().toList(), inAllPharmacies);
        sb.append("\n\n");

        if (coupon.getStartDate() != null && coupon.getEndDate() != null) {
            sb.append("*–° ").append(coupon.getStartDate().format(DateTimeFormatter.ofPattern("dd.MM.yyyy"))).append(" –ø–æ ").append(coupon.getEndDate().format(DateTimeFormatter.ofPattern("dd.MM.yyyy"))).append("*\n\n");
        }

        sb.append("*–ö—É–ø–æ–Ω –¥–µ–π—Å—Ç–≤—É–µ—Ç –≤ —Ç–µ—á–µ–Ω–∏–µ ").append(durationInMinutes).append(" –º–∏–Ω—É—Ç. –ê–∫—Ç–∏–≤–∏—Ä—É–π—Ç–µ –µ–≥–æ –ø—Ä–∏ –∫–∞—Å—Å–µ.*");

        sb.append("\n\n");

        sb.append("_*–í—ã –º–æ–∂–µ—Ç–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω –∫—É–ø–æ–Ω –Ω–∞ –æ–¥–Ω—É –ø–æ–∫—É–ø–∫—É_");

        return sb.toString();
    }

